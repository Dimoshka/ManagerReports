package com.syject.managerreports.data.adapter;

import android.support.v7.widget.RecyclerView;
import android.view.LayoutInflater;
import android.view.View;
import android.view.ViewGroup;
import android.widget.TextView;

import com.syject.managerreports.R;
import com.syject.managerreports.data.db.ReportHistory;
import com.syject.managerreports.ui.listFragment.OnListFragmentInteractionListener;

import java.util.List;

public class ReportHistoryRecyclerViewAdapter extends RecyclerView.Adapter<ReportHistoryRecyclerViewAdapter.ViewHolder> {

    private final List<ReportHistory> mValues;
    private final OnListFragmentInteractionListener mListener;

    public ReportHistoryRecyclerViewAdapter(List<ReportHistory> reports, OnListFragmentInteractionListener listener) {
        mValues = reports;
        mListener = listener;
    }

    @Override
    public ViewHolder onCreateViewHolder(ViewGroup parent, int viewType) {
        View view = LayoutInflater.from(parent.getContext())
                .inflate(R.layout.fragment_item, parent, false);
        return new ViewHolder(view);
    }

    @Override
    public void onBindViewHolder(final ViewHolder holder, int position) {
        holder.mItem = mValues.get(position);
        holder.mIdView.setText(mValues.get(position).user.name);
        holder.mContentView.setText(mValues.get(position).report.name);

        holder.mView.setOnClickListener(new View.OnClickListener() {
            @Override
            public void onClick(View v) {
                if (null != mListener) {
                    // Notify the active callbacks interface (the activity, if the
                    // fragment is attached to one) that an item has been selected.
                    //mListener.onListFragmentInteraction(holder.mItem);
                }
            }
        });
    }

    @Override
    public int getItemCount() {
        return mValues.size();
    }

    public class ViewHolder extends RecyclerView.ViewHolder {
        public final View mView;
        public final TextView mIdView;
        public final TextView mContentView;
        public ReportHistory mItem;

        public ViewHolder(View view) {
            super(view);
            mView = view;
            mIdView = (TextView) view.findViewById(R.id.id);
            mContentView = (TextView) view.findViewById(R.id.content);
        }

        @Override
        public String toString() {
            return super.toString() + " '" + mContentView.getText() + "'";
        }
    }
}
